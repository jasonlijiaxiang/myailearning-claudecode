def main():
    apply = "--apply" in sys.argv
    hit = miss = 0
    for mod, items in BOUNDS.items():
        path = os.path.join(ROOT, mod, "index.html")
        text = io.open(path, encoding="utf-8").read()
        for key, extra in items:
            i = text.find(key)
            if i < 0:
                print("  [找不到] %s :: %s" % (mod, key))
                miss += 1
                continue
            # 落位必须在证据注内部——键若在别处先出现，find 会插到错的地方
            st = text.rfind('<p class="ev">', 0, i)
            if st < 0 or text.find("</p>", st) < i:
                print("  [不在证据注里] %s :: %s" % (mod, key))
                miss += 1
                continue
            if text.count(key) > 1:
                print("  [键不唯一] %s :: %s（出现 %d 次）" % (mod, key, text.count(key)))
                miss += 1
                continue
            j = text.find("</p>", i)
            if j < 0:
                print("  [没闭合] %s :: %s" % (mod, key))
                miss += 1
                continue
            if extra[:12] in text[i:j]:
                print("  [已有] %s :: %s" % (mod, key))
                continue
            text = text[:j] + "\n        <b>不可外推边界</b>：" + extra + text[j:]
            hit += 1
        if apply:
            io.open(path, "w", encoding="utf-8").write(text)
    print("\n补入 %d 条边界，%d 条落位失败。%s"
          % (hit, miss, "已落盘。" if apply else "预演，未落盘（加 --apply）。"))
    return 1 if miss else 0


if __name__ == "__main__":
    sys.exit(main())
